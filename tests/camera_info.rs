#[test]
fn parse_x_m5_device_info() {
    let info = CameraInfo::parse(&X_M5_DEVICE_INFO).unwrap();

    assert_eq!(info.manufacturer, "FUJIFILM");
    assert_eq!(info.model, "X-M5");
    assert_eq!(info.device_version, "1.20");
    assert_eq!(info.serial_number, "ABCDEFGHJKLMNPQRSTUVWXYZ123456");
    assert_eq!(info.operations_supported.len(), 20);
    assert_eq!(info.device_properties_supported.len(), 61);
    assert_eq!(info.capture_formats.len(), 5);
    assert_eq!(info.image_formats.len(), 7);
}

#[test]
fn parse_x_m5_operations() {
    let info = CameraInfo::parse(&X_M5_DEVICE_INFO).unwrap();

    // Verify some known PTP operations are present.
    assert!(info.operations_supported.contains(&0x1001)); // GetDeviceInfo
    assert!(info.operations_supported.contains(&0x1002)); // OpenSession
    assert!(info.operations_supported.contains(&0x1003)); // CloseSession
    assert!(info.operations_supported.contains(&0x1009)); // GetObject
    assert!(info.operations_supported.contains(&0x900C)); // Fuji SendObjectInfo
    assert!(info.operations_supported.contains(&0x900D)); // Fuji SendObject
}

#[test]
fn parse_truncated_data_fails() {
    assert!(CameraInfo::parse(&X_M5_DEVICE_INFO[..10]).is_err());
}
